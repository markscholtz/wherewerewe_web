  factory :user do
    sequence(:email) {|n| "user#{n}@example.com" }
    password 'password'
    password_confirmation 'password'
  end

  factory :viewing do
    episode
    season
    series
    user
  end

  factory :series do
    sequence(:tvdb_id)
    name 'Living the high life'
    overview 'A series about two Capetonians...'
    last_updated 1.day.ago
  end

  factory :season do
    sequence(:tvdb_id)
    sequence(:number)

    series
  end

  factory :episode do
    sequence(:tvdb_id)
    name 'Two bros, just chillin'
    overview 'Two bros go to the beach ... to chill'
    last_updated 1.day.ago
    sequence(:number)

    series
    season
  end
end
